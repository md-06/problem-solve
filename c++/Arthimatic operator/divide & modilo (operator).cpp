#include<iostream>
using namespace std;

int main()
{
	cout<<"Quitent"<<endl;
	cout<<7/3<<endl;
	cout<<-7/3<<endl;
	cout<<7/-3<<endl;
	cout<<-7/-3<<endl;
 

	cout<<"Remainder"<<endl;	
// if dividend +ve than remainder +ve and 
// when dividend -ve than remainder -ve.
	cout<<7%3<<endl;
	cout<<-7%3<<endl;
	cout<<7%-3<<endl;
	cout<<-7%-3<<endl;
	
	return 0;
}